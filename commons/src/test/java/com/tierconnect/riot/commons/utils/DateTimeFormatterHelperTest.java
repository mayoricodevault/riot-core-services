    @Test
    public void dateTimeFormatterLiteralTest() {
        List<String> dateShort = Arrays.asList(
                "31 Jan 2017", "29 Feb 2016", "31 Mar 2017",
                "30 Apr 2017", "31 May 2017", "30 Jun 2017",
                "31 Jul 2017", "31 Aug 2017", "30 Sep 2017",
                "31 Oct 2017", "31 Nov 2017", "31 Dec 2017");

        for (String date : dateShort) {
            assertEquals("Check error with [" + date + "] lowercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy),
                    LocalDate.parse(date.toLowerCase(), DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy));
            assertEquals("Check error with [" + date + "] uppercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy),
                    LocalDate.parse(date.toUpperCase(), DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy));
        }

        List<String> dateFull = Arrays.asList(
                "31 January 2017", "29 February 2016", "31 March 2017",
                "30 April 2017", "31 May 2017", "30 June 2017",
                "31 July 2017", "31 August 2017", "30 September 2017",
                "31 October 2017", "31 November 2017", "31 December 2017");

        for (String date : dateFull) {
            assertEquals("Check error with [" + date + "] lowercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy),
                    LocalDate.parse(date.toLowerCase(), DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy));
            assertEquals("Check error with [" + date + "] uppercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy),
                    LocalDate.parse(date.toUpperCase(), DateTimeFormatterHelper.DF_dd_MMMorMMMM_yyyy));
        }

        List<String> dateJavaToString = Arrays.asList(
                "Tue May 30 11:40:51 EST 2017", "Wed May 31 11:40:51 EST 2017", "fri Jun 16 11:40:51 EST 2017");

        for (String date : dateJavaToString) {
            assertEquals("Check error with [" + date + "] lowercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.DATE_FORMAT_E_MMM_dd_HH_mm_ss_z_yyyy),
                    LocalDate.parse(date.toLowerCase(), DateTimeFormatterHelper.DATE_FORMAT_E_MMM_dd_HH_mm_ss_z_yyyy));
            assertEquals("Check error with [" + date + "] uppercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.DATE_FORMAT_E_MMM_dd_HH_mm_ss_z_yyyy),
                    LocalDate.parse(date.toUpperCase(), DateTimeFormatterHelper.DATE_FORMAT_E_MMM_dd_HH_mm_ss_z_yyyy));
        }

        List<String> dateRFC_2822 = Arrays.asList(
                "Sat, 13 Mar 2010 11:29:05 -0800",
                "Tue, 30 May 2017 17:04:34 -0400",
                "Tue, 30 May 2017 17:04:34 +0000");

        for (String date : dateRFC_2822) {
            assertEquals("Check error with [" + date + "] lowercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.START_WITH_EEE_dd_MMM_yyyy_HH_mm_ss_Z),
                    LocalDate.parse(date.toLowerCase(), DateTimeFormatterHelper.START_WITH_EEE_dd_MMM_yyyy_HH_mm_ss_Z));
            assertEquals("Check error with [" + date + "] uppercase",
                    LocalDate.parse(date, DateTimeFormatterHelper.START_WITH_EEE_dd_MMM_yyyy_HH_mm_ss_Z),
                    LocalDate.parse(date.toUpperCase(), DateTimeFormatterHelper.START_WITH_EEE_dd_MMM_yyyy_HH_mm_ss_Z));
        }
    }

    @Test
    public void equalsDateTest() {
        List<String> dateFormatList = Arrays.asList(
                "01/01/2015", "01-01-2015",
                "02/02/2015", "02-02-2015",
                "03/03/2015", "03-03-2015"
        );
        for (String dateFormat : dateFormatList) {
            assertEquals("Check date : " + dateFormat
                    , LocalDateTime.parse(dateFormat, DateTimeFormatterHelper.DTF_SW_dd_MM_yyyy_FORMAT_1)
                    , LocalDateTime.parse(dateFormat, DateTimeFormatterHelper.DTF_SW_MM_dd_yyyy_FORMAT_1));
        }

        assertEquals(
                LocalDateTime.parse("02/02/2017", DateTimeFormatterHelper.DTF_SW_dd_MM_yyyy_FORMAT_1),
                LocalDateTime.parse("02-02-2017", DateTimeFormatterHelper.DTF_SW_MM_dd_yyyy_FORMAT_1));

        assertEquals(
                LocalDateTime.parse("02/03/2017", DateTimeFormatterHelper.DTF_SW_dd_MM_yyyy_FORMAT_1),
                LocalDateTime.parse("03-02-2017", DateTimeFormatterHelper.DTF_SW_MM_dd_yyyy_FORMAT_1));
        assertEquals(
                LocalDateTime.parse("10-05-2017", DateTimeFormatterHelper.DTF_SW_dd_MM_yyyy_FORMAT_1),
                LocalDateTime.parse("05/10/2017", DateTimeFormatterHelper.DTF_SW_MM_dd_yyyy_FORMAT_1));
    }

